    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        int selectedRow = tblStaff.getSelectedRow();
        if (selectedRow != -1) {
            JDialog.setDefaultLookAndFeelDecorated(true);
            int response = JOptionPane.showConfirmDialog(null, "Are you sure to delete?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (response == JOptionPane.NO_OPTION) {
                System.out.println("No button clicked");
            } else if (response == JOptionPane.YES_OPTION) {
                StaffService ss = new StaffService();
                Staff s = new Staff();
                Object o = tblStaff.getModel().getValueAt(tblStaff.getSelectedRow(), 0);
                s.setId(Long.parseLong(o.toString()));

                //System.out.println( Long.parseLong(o.toString()));
                ss.delete(s);
                fiilingTable(ListTypeEnum.STAFF);

            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select a row");
            return;
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

    
