    private void fiilingTable(ListTypeEnum typeEnum) {
        String[] headers;
        String[][] data;

        if (typeEnum == typeEnum.STAFF) {
            List<Staff> listStaff = staffService.getAll();
            headers = new String[]{"Id", "First Name", "Last Name"};
            data = new String[listStaff.size()][3];

            for (int i = 0; i < listStaff.size(); i++) {
                data[i][0] = listStaff.get(i).getId().toString();
                data[i][1] = listStaff.get(i).getName();
                data[i][2] = listStaff.get(i).getSurname();

            }



        } else {
            List<Staff> listStaff = staffService.getAll();
            headers = new String[]{"Id", "First Name", "Last Name"};
            data = new String[listStaff.size()][3];

            for (int i = 0; i < listStaff.size(); i++) {
                data[i][0] = listStaff.get(i).getId().toString();
                data[i][1] = listStaff.get(i).getName();
                data[i][2] = listStaff.get(i).getSurname();

            }
        }

        tblStaff.setModel(new DefaultTableModel(data, headers));
    }
